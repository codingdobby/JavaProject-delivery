package JaBae.admin.VO;

public class MemberMgtSellerVO {
	private String idSeller;
	private String nameSeller;
	private String joinedDateSeller;
	private String freeTicket;

	public String getIdSeller() {
		return idSeller;
	}

	public void setIdSeller(String idSeller) {
		this.idSeller = idSeller;
	}

	public String getNameSeller() {
		return nameSeller;
	}

	public void setNameSeller(String nameSeller) {
		this.nameSeller = nameSeller;
	}

	public String getJoinedDateSeller() {
		return joinedDateSeller;
	}

	public void setJoinedDateSeller(String joinedDateSeller) {
		this.joinedDateSeller = joinedDateSeller;
	}

	public String getFreeTicket() {
		return freeTicket;
	}

	public void setFreeTicket(String freeTicket) {
		this.freeTicket = freeTicket;
	}
}
