package JaBae.admin.VO;

public class MemberMgtCustomerVO {
	private String idCustomer;
	private String nameCustomer;
	private String birthCustomer;
	private String lastOrderCustomer;
	private String joinedDateCustomer;
	private String pointCustomer;

	public String getIdCustomer() {
		return idCustomer;
	}

	public void setIdCustomer(String idCustomer) {
		this.idCustomer = idCustomer;
	}

	public String getNameCustomer() {
		return nameCustomer;
	}

	public void setNameCustomer(String nameCustomer) {
		this.nameCustomer = nameCustomer;
	}

	public String getBirthCustomer() {
		return birthCustomer;
	}

	public void setBirthCustomer(String birthCustomer) {
		this.birthCustomer = birthCustomer;
	}

	public String getLastOrderCustomer() {
		return lastOrderCustomer;
	}

	public void setLastOrderCustomer(String lastOrderCustomer) {
		this.lastOrderCustomer = lastOrderCustomer;
	}

	public String getJoinedDateCustomer() {
		return joinedDateCustomer;
	}

	public void setJoinedDateCustomer(String joinedDateCustomer) {
		this.joinedDateCustomer = joinedDateCustomer;
	}

	public String getPointCustomer() {
		return pointCustomer;
	}

	public void setPointCustomer(String pointCustomer) {
		this.pointCustomer = pointCustomer;
	}

}
