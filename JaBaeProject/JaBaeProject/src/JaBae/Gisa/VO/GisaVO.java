package JaBae.Gisa.VO;

public class GisaVO {
	private String orderNo;
	private String objectName;
	private String addrOrderer;
	private String nameOrderer;
	private String telOrderer;
	private String startDate;

	public String getOrderNo() {
		return orderNo;
	}
	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}
	public String getObjectName() {
		return objectName;
	}
	public void setObjectName(String objectName) {
		this.objectName = objectName;
	}
	public String getAddrOrderer() {
		return addrOrderer;
	}
	public void setAddrOrderer(String addrOrderer) {
		this.addrOrderer = addrOrderer;
	}
	public String getNameOrderer() {
		return nameOrderer;
	}
	public void setNameOrderer(String nameOrderer) {
		this.nameOrderer = nameOrderer;
	}
	public String getTelOrderer() {
		return telOrderer;
	}
	public void setTelOrderer(String telOrderer) {
		this.telOrderer = telOrderer;
	}
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
}
